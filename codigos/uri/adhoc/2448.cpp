#include <bits/stdc++.h>

using namespace std;

int main(int argc, char const *argv[]) {
  int n,m;
  scanf("%d %d",&n,&m );
  vector<int> v(n);
  map<int, int> mapa;
  for (size_t i = 0; i < n; i++) {
    cin >>v[i];
    mapa.insert(pair<int,int>(v[i],i));
  }
  int aux;
  int rest = 0;
  int pos = 0;
  for (size_t i = 0; i < m; i++) {
    cin >> aux;
    rest+= abs(pos-mapa[aux]);
    pos = mapa[aux];
  }
  cout << rest << endl;

  return 0;
}


